package com.example.lab_04_04_evenodd;

public class Lab_04_04_EvenOdd {
        public static void main(String[] args) {
            int inputNumber = 42;

            if (inputNumber % 2 == 0) {
                System.out.println(inputNumber + " is even.");
            } else {
                System.out.println(inputNumber + " is odd.");
            }
        }


}

